package com.design.mode.creational.factorymethod.product;

/**
 * 产品抽象接口
 *
 * 在工厂方法模式中，Product 是“产品层”的统一抽象。
 * 客户端只依赖该接口，不直接依赖具体产品实现，从而降低耦合。
 *
 * 设计要点：
 * 1. 定义产品的公共行为规范，约束所有具体产品的能力边界
 * 2. 让工厂层仅返回 Product 抽象，屏蔽具体产品创建细节
 * 3. 支持在不修改客户端逻辑的前提下扩展新的具体产品
 *
 * 扩展建议：
 * - 当产品能力增加时，优先在接口层新增抽象方法，并由具体产品实现
 * - 若存在可选能力，可配合默认方法或拆分子接口避免接口臃肿
 */
public interface Product {

    /**
     * 使用产品能力
     *
     * 该方法用于体现“产品被创建后的业务行为”。
     * 不同的具体产品会给出各自实现，以表达功能差异。
     *
     * 说明：
     * - 方法名保持统一，便于客户端进行多态调用
     * - 具体实现中可接入真实业务逻辑，而不仅是控制台输出
     */
    void use();
}

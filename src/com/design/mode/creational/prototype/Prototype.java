package com.design.mode.creational.prototype;

/**
 * 原型接口（Prototype）
 * 
 * 原型模式中的抽象原型角色，声明一个克隆自身的接口。
 * 这是原型模式的核心接口，所有具体原型类都需要实现这个接口。
 * 
 * 原型模式的核心思想：
 * - 用原型实例指定创建对象的种类
 * - 通过拷贝这些原型创建新的对象
 * - 允许通过复制现有对象来生成新对象，而无需重新初始化
 * 
 * 原型模式的核心实现方式：
 * 1. 使用 Object 的 clone() 方法（浅拷贝）
 * 2. 使用序列化和反序列化（深拷贝）
 * 3. 使用 JSON 序列化（深拷贝）
 * 4. 手动实现深拷贝
 * 
 * 原型模式的设计原则：
 * - 组合优于继承：通过复制而非继承来创建对象
 * - 依赖倒置原则：依赖于抽象（Prototype）而非具体类
 * 
 * 适用场景：
 * 1. 需要复制对象，但不想重复初始化对象，例如对象创建代价较大时
 * 2. 需要通过复制操作来生成对象，例如保护性拷贝
 * 3. 需要避免子类与创建过程耦合时（如工厂方法中）
 * 4. 系统需要独立于它的产品创建、构成和表示时
 * 5. 一个对象需要提供给其他对象访问，而且各个调用者可能都需要修改其值时
 * 
 * 原型模式的优点：
 * - 性能优良：Java的clone方法是本地方法，基于内存二进制流复制，比直接new性能好
 * - 逃避构造函数的约束：不会触发构造函数的执行
 * - 简化创建：可以在运行时动态获取对象的类型
 * - 保护性拷贝：返回对象的拷贝，防止外部修改内部状态
 * 
 * 原型模式的缺点：
 * - 需要配备克隆方法，对于已有类可能需要修改源代码
 * - 实现深拷贝需要比较复杂的代码：每个引用类型都需要实现Cloneable
 * - 必须实现 Cloneable 接口，否则会抛出 CloneNotSupportedException
 * - 逃避类构造函数：可能导致对象的初始化不完整
 * 
 * 浅拷贝 vs 深拷贝：
 * - 浅拷贝：只复制对象本身，不复制引用类型的字段
 * - 深拷贝：复制对象本身及所有引用类型的字段
 */
public interface Prototype<T> {

    /**
     * 克隆方法
     * 
     * 这是原型模式的核心方法，用于创建当前对象的拷贝。
     * 具体实现可以是浅拷贝或深拷贝，取决于具体的实现类。
     * 
     * 实现方式：
     * 1. 浅拷贝：使用 Object.clone()，只复制基本类型和引用
     * 2. 深拷贝：使用序列化或手动克隆，复制所有引用类型
     * 
     * @return 克隆的对象，应该是当前对象的完整副本
     */
    T clone();
}

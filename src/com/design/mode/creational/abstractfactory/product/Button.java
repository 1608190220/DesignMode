package com.design.mode.creational.abstractfactory.product;

/**
 * 抽象产品A：按钮
 *
 * 抽象工厂模式的定义：
 * 1. 提供一个创建一系列相关或相互依赖对象的接口，而无需指定它们具体的类
 * 2. 是工厂方法模式的升级版，用于创建一组相关的产品
 * 3. 每个具体工厂负责创建一个产品族中的所有产品
 *
 * 本接口定义按钮组件在业务层可见的统一能力。
 * 无论底层是 Windows 还是 Mac 风格，客户端都通过该接口进行调用。
 */
public interface Button {

    /**
     * 按钮的渲染方法
     *
     * 用于输出或绘制按钮外观，具体样式由具体产品决定。
     */
    void render();

    /**
     * 按钮的点击事件
     *
     * 用于模拟按钮交互行为，不同平台可有不同反馈。
     */
    void onClick();
}

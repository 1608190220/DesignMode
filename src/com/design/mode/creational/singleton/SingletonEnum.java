package com.design.mode.creational.singleton;

/**
 * 单例模式 - 枚举方式
 * 
 * 特点：
 * 1. 这是 JDK1.5 之后提供的方式，是实现单例的最佳方式
 * 2. 不仅能避免多线程同步问题，还能防止反序列化重新创建新的对象
 * 3. 枚举在 Java 中本身就是单例的，由 JVM 保证线程安全和唯一性
 * 4. 推荐使用，代码最简洁，最安全
 * 
 * 枚举单例的优势：
 * - 线程安全：由 JVM 保证，不需要自己处理同步
 * - 防止反射攻击：无法通过反射创建枚举实例
 * - 防止序列化破坏：枚举的序列化机制特殊，反序列化后仍然是同一个实例
 * - 代码简洁：几行代码就实现了完美的单例
 * 
 * 《Effective Java》推荐使用枚举实现单例模式。
 * 
 * 说明：
 * 调用方式：SingletonEnum.INSTANCE.doSomething();
 */
public enum SingletonEnum {

    /**
     * 定义一个枚举元素，它就是单例的一个实例
     * 
     * 枚举类型在 Java 中的特性：
     * - 每个枚举常量都是唯一的实例
     * - 由 JVM 保证在整个应用中只有一个实例
     * - 线程安全，由类加载机制保证
     */
    INSTANCE;

    /**
     * 业务方法
     * 
     * 枚举类可以像普通类一样定义方法和字段。
     */
    public void doSomething() {
        System.out.println("枚举单例正在执行任务...");
    }
}

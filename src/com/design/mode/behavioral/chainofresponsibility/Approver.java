package com.design.mode.behavioral.chainofresponsibility;

/**
 * 审批者接口（责任链中的抽象处理者）
 *
 * 责任链模式（Chain of Responsibility Pattern）详解：
 *
 * 一、模式意图
 * 使多个对象都有机会处理请求，从而避免请求的发送者和接收者之间的耦合关系。
 * 将这些对象连成一条链，并沿着这条链传递请求，直到有一个对象处理它为止。
 *
 * 二、模式结构
 * 1. 处理器（Handler）: 定义处理请求的接口，这里是Approver
 * 2. 具体处理器（Concrete Handler）: 处理它负责的请求，可以访问它的后继者，
 *    如果可以处理请求就处理，否则将请求转发给后继者，这里是TeamLeader、DepartmentManager、GeneralManager
 * 3. 客户端（Client）: 向链上的具体处理器对象提交请求
 *
 * 三、适用场景
 * 1. 有多个对象可以处理一个请求，哪个对象处理该请求运行时自动确定
 * 2. 想在不明确指定接收者的情况下，向多个对象中的一个提交请求
 * 3. 可动态指定一组对象处理请求
 *
 * 四、优缺点
 * 优点：
 * - 降低耦合度：将请求的发送者和接收者解耦
 * - 简化对象：对象不需要知道链的结构
 * - 增强灵活性：可以动态地增加或修改处理链
 * - 方便扩展：增加新的具体处理器很容易
 *
 * 缺点：
 * - 请求可能得不到处理：请求可能到达链的末端都得不到处理
 * - 不易调试：请求可能经过多个处理器，调试时较复杂
 * - 性能问题：如果链太长，可能会影响性能
 *
 * 五、实际应用
 * - Java Servlet中的Filter链
 * - Spring Security中的过滤器链
 * - 审批流程、日志记录链、异常处理链
 *
 * 在本示例中，Approver承担以下职责：
 * 1. 约定链路连接方式：每个审批者都可以指向“下一个审批者”
 * 2. 约定请求入口：客户端只需要调用统一的处理方法即可
 * 3. 隐藏分发细节：客户端无需关心究竟由谁处理请求
 *
 * 设计要点：
 * - 接口只保留最小必要行为，具体审批规则由实现类决定
 * - 通过面向抽象编程，便于后续新增审批角色（如HR、副总等）
 * - 客户端依赖接口而非具体类，降低调用方与处理方的耦合
 */
public interface Approver {
    /**
     * 设置下一个审批者（建立责任链节点之间的连接关系）
     *
     * 通常在系统初始化阶段由客户端统一组装链路。
     * 例如：组长 -> 部门经理 -> 总经理。
     * 当当前审批者无法处理请求时，请求会沿该连接继续向后传递。
     *
     * @param nextApprover 下一个审批者
     */
    void setNextApprover(Approver nextApprover);

    /**
     * 处理请假申请（责任链统一入口）
     *
     * 方法语义：
     * - 若当前审批者权限足够：直接处理并结束传递
     * - 若权限不足：转发给下一个审批者
     * - 若没有后继者：请求无法被处理，进入兜底分支
     *
     * 注意：
     * - 客户端一般只调用链头节点的该方法
     * - 具体审批阈值和输出文案由实现类定义
     *
     * @param request 请假申请
     */
    void processLeaveRequest(LeaveRequest request);
}

package com.design.mode.behavioral.strategy;

/**
 * 策略接口（Strategy）
 * 
 * 策略模式中的抽象策略角色，定义算法族的统一行为契约。
 * 这是策略模式的核心接口，所有具体策略类都需要实现这个接口。
 * 
 * 策略模式的核心思想：
 * - 定义一系列算法，把它们一个个封装起来
 * - 并且使它们可以相互替换
 * - 使得算法可以独立于使用它的客户端而变化
 * 
 * 策略模式的设计原则体现：
 * - 开闭原则（Open/Closed Principle）：对扩展开放，对修改关闭
 * - 依赖倒置原则（Dependency Inversion Principle）：依赖于抽象而非具体
 * - 合成复用原则（Composite Reuse Principle）：优先使用组合而非继承
 * 
 * 设计说明：
 * 1. 客户端与上下文只依赖该抽象，不依赖具体算法实现。
 * 2. 新增策略时仅需实现本接口，符合开闭原则。
 * 3. 不同策略可在运行时互相替换，达到"同一入口、不同实现"的效果。
 * 
 * 适用场景：
 * - 当一个系统需要动态地在几种算法中选择一种时
 * - 当一个对象有很多行为，而使用 if-else 或 switch-case 语句来选择时
 * - 当需要算法的变体可以被客户端独立选择时
 */
public interface Strategy {
    /**
     * 执行当前策略算法
     * 
     * 该方法代表算法入口，具体步骤由实现类决定。
     * 这是策略模式的核心方法，Context 会调用这个方法来执行具体的策略。
     * 
     * 方法设计：
     * - 统一的方法签名，确保所有策略都有相同的调用方式
     * - 具体算法由实现类提供，Context 不关心算法细节
     * - 支持运行时动态切换策略
     */
    void execute();
}

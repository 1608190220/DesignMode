package com.design.mode.behavioral.strategy;

/**
 * 具体策略C（Concrete Strategy C）
 * 
 * 策略模式中的具体策略角色，提供"插入排序"场景的策略实现。
 * 这是策略模式的具体实现类，实现了 Strategy 接口。
 * 
 * 具体策略角色的职责：
 * - 实现 Strategy 接口
 * - 提供具体的算法实现
 * - 可以被 Context 持有和调用
 * 
 * 插入排序算法特点：
 * - 时间复杂度：O(n²)
 * - 空间复杂度：O(1)
 * - 稳定排序
 * - 适合小规模数据或基本有序的数据
 * - 适合在线排序（数据逐到来）
 * 
 * 说明：
 * 该策略适合演示"增量构建有序区间"的排序思想，
 * 进一步体现策略模式下算法可替换性。
 */
public class ConcreteStrategyC implements Strategy {
    /**
     * 执行插入排序策略（示意版）
     * 
     * 通过输出关键步骤描述算法流程：
     * 1. 从未排序区取元素
     * 2. 插入到已排序区合适位置
     * 3. 重复直到整体有序
     * 
     * 这是策略模式的核心方法，提供具体的算法实现。
     * Context 会调用这个方法来执行具体的排序策略。
     * 
     * 插入排序算法流程说明：
     * 1. 从第一个元素开始，该元素可以认为已经被排序
     * 2. 取出下一个元素，在已经排序的元素序列中从后向前扫描
     * 3. 如果该元素（已排序）大于新元素，将该元素移到下一位置
     * 4. 重复步骤3，直到找到已排序的元素小于或者等于新元素的位置
     * 5. 将新元素插入到该位置后
     * 6. 重复步骤2-5
     */
    @Override
    public void execute() {
        System.out.println("执行策略C：使用插入排序算法");
        // 以下为流程示意，便于观察与其他策略的差异
        System.out.println("步骤1: 从第二个元素开始遍历");
        System.out.println("步骤2: 将当前元素插入到已排序部分的正确位置");
        System.out.println("步骤3: 重复直到排序完成");
        System.out.println("插入排序完成，时间复杂度 O(n²)");
    }
}

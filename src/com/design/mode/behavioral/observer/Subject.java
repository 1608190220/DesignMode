package com.design.mode.behavioral.observer;

/**
 * 主题接口（Subject）
 * 
 * 观察者模式中的抽象主题角色，定义管理观察者和通知它们变化的方法。
 * 观察者模式的核心思想：维护观察者列表并在状态变化时通知它们。
 * 
 * 观察者模式的核心思想：
 * - 定义对象间的一对多依赖关系
 * - 当一个对象状态发生变化时，所有依赖它的对象都会得到通知并被自动更新
 * - 也称为发布-订阅（Publish-Subscribe）模式
 * 
 * 观察者模式的设计原则体现：
 * - 开闭原则（Open/Closed Principle）：新增观察者不需要修改主题
 * - 依赖倒置原则（Dependency Inversion Principle）：依赖于抽象（Observer）而非具体类
 * - 合成复用原则（Composite Reuse Principle）：优先使用组合而非继承
 * 
 * 观察者模式的适用场景：
 * - 当一个抽象模型有两个方面，其中一个方面依赖于另一方面时
 * - 当对一个对象的改变需要同时改变其他对象，而不知道具体有多少对象有待改变时
 * - 当一个对象必须通知其他对象，而它又不能假定其他对象是谁时
 * 
 * 观察者模式的优点：
 * - 主题和观察者之间是抽象耦合的
 * - 支持广播通信
 * - 符合开闭原则
 * 
 * 观察者模式的缺点：
 * - 如果一个主题有很多观察者，通知所有观察者会花费很多时间
 * - 如果在观察者和主题之间有循环依赖，可能会导致系统崩溃
 * - 观察者不知道主题是怎么发生变化的，只知道主题发生了变化
 */
public interface Subject {
    
    /**
     * 向主题添加一个观察者
     * 
     * 这是观察者模式的核心方法之一，用于注册观察者。
     * 观察者通过调用这个方法来订阅主题的状态变化。
     * 
     * 设计要点：
     * - 接收 Observer 接口的引用，而非具体类
     * - 将观察者添加到观察者列表中
     * - 支持运行时动态添加观察者
     * 
     * @param observer 要添加的观察者
     */
    void attach(Observer observer);
    
    /**
     * 从主题中移除一个观察者
     * 
     * 这是观察者模式的核心方法之一，用于取消注册观察者。
     * 观察者通过调用这个方法来取消订阅主题的状态变化。
     * 
     * 设计要点：
     * - 接收 Observer 接口的引用，而非具体类
     * - 将观察者从观察者列表中移除
     * - 支持运行时动态移除观察者
     * 
     * @param observer 要移除的观察者
     */
    void detach(Observer observer);
    
    /**
     * 通知所有观察者状态变化
     * 
     * 这是观察者模式的核心方法之一，用于通知所有观察者。
     * 当主题状态发生变化时，调用这个方法来通知所有观察者。
     * 
     * 设计要点：
     * - 遍历观察者列表
     * - 调用每个观察者的 update() 方法
     * - 可以传递额外的消息或数据
     */
    void notifyObservers();
}

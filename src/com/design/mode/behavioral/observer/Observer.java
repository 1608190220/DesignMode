package com.design.mode.behavioral.observer;

/**
 * 观察者接口（Observer）
 * 
 * 观察者模式中的抽象观察者角色，定义当主题发生变化时将被调用的更新方法。
 * 观察者模式的核心思想：提供观察者对主题变化做出反应的方式。
 * 
 * 抽象观察者角色的职责：
 * - 定义一个更新接口，在得到主题的通知时更新自己
 * - 可以通过主题引用获取主题的状态
 * - 可以接收来自主题的消息或数据
 * 
 * 推模型 vs 拉模型：
 * - 推模型：主题主动向观察者推送详细信息（本示例使用推模型）
 * - 拉模型：主题只发送少量信息，观察者主动从主题拉取信息
 * 
 * 本示例使用推模型，通过 message 参数传递详细信息。
 */
public interface Observer {
    
    /**
     * 当主题状态发生变化时由主题调用的更新方法
     * 
     * 这是观察者模式的核心方法，每个具体观察者类都需要实现这个方法。
     * 当主题状态发生变化时，主题会调用这个方法来通知观察者。
     * 
     * 推模型实现：
     * - 主题通过 message 参数向观察者推送详细信息
     * - 观察者可以直接使用推送的信息，不需要再查询主题
     * - 优点：观察者不需要知道主题的细节
     * - 缺点：推送的信息可能不完整或过多
     * 
     * @param subject 发生变化的主题，观察者可以通过它获取更多信息
     * @param message 来自主题的可选消息或数据，推模型的核心
     */
    void update(Subject subject, Object message);
}

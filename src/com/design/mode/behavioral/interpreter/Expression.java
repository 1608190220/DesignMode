package com.design.mode.behavioral.interpreter;

/**
 * 抽象表达式接口，定义解释器模式中所有语法节点共享的求值行为。
 *
 * 在当前示例中，每一个具体表达式对象都实现该接口，并在
 * {@link #interpret(Context)} 中完成各自的计算逻辑：
 * - 终结符表达式：直接给出结果，如常量或变量取值
 * - 非终结符表达式：组合并递归计算子表达式结果
 *
 * 该接口的核心价值在于统一调用入口，使客户端只依赖抽象而不依赖具体实现，
 * 从而可以通过“组装表达式树”的方式描述并执行不同算术规则。
 */
public interface Expression {
    /**
     * 对当前表达式节点执行解释并返回计算结果。
     *
     * 执行时机通常发生在表达式树根节点调用解释后，递归下沉到每个子节点，
     * 最终由叶子节点先返回值，再逐层向上合并得到最终结果。
     *
     * @param context 上下文对象，提供变量名到具体数值的映射能力
     * @return 当前表达式在给定上下文中的整数结果
     */
    int interpret(Context context);
}

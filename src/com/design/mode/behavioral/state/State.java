package com.design.mode.behavioral.state;

/**
 * 状态接口（State）
 * 
 * 状态模式中的抽象状态角色，定义所有具体状态必须实现的方法。
 * 状态模式的核心思想：将对象行为封装到不同的状态类中。
 * 
 * 状态模式的核心思想：
 * - 允许一个对象在其内部状态改变时改变它的行为
 * - 对象看起来似乎修改了它的类
 * - 把对象的行为和状态封装在一起，使得状态的转换更加清晰
 * 
 * 状态模式的设计原则体现：
 * - 开闭原则（Open/Closed Principle）：新增状态不需要修改 Context 和其他状态
 * - 单一职责原则（Single Responsibility Principle）：每个状态类只负责自己的行为
 * - 依赖倒置原则（Dependency Inversion Principle）：依赖于抽象（State）而非具体类
 * 
 * 状态模式的适用场景：
 * - 一个对象的行为取决于它的状态，并且它必须在运行时刻根据状态改变它的行为
 * - 一个操作中含有庞大的多分支的条件语句，且这些分支依赖于该对象的状态
 * - 当一个对象的状态转换非常复杂，或者状态转换的逻辑需要经常变化时
 * 
 * 状态模式的优点：
 * - 状态的转换逻辑清晰，易于理解和维护
 * - 避免了大量的条件判断语句
 * - 符合开闭原则，新增状态简单
 * - 封装了状态转换规则
 * 
 * 状态模式的缺点：
 * - 会增加系统的类和对象的个数
 * - 状态模式的结构与实现都较为复杂，如果使用不当会导致程序结构和代码的混乱
 * - 对开闭原则的支持并不太好，对于可以切换状态的状态模式，增加新的状态类需要修改那些负责状态转换的代码
 */
public interface State {
    
    /**
     * 处理当前状态下的行为
     * 
     * 这是状态模式的核心方法，每个具体状态类都需要实现这个方法。
     * 在这个方法中，状态类可以：
     * 1. 执行当前状态下的业务逻辑
     * 2. 决定是否需要切换到其他状态
     * 3. 如果需要切换，通过 context.setState() 来切换状态
     * 
     * 设计要点：
     * - 接收 Context 对象，用于状态切换
     * - 在方法内部决定是否切换状态
     * - 通过 context.setState() 来切换状态
     * 
     * @param context 用于状态切换的上下文对象
     */
    void handle(Context context);
    
    /**
     * 获取状态名称
     * 
     * 用于调试和日志输出，方便查看当前状态。
     * 
     * @return 状态名称，如"初始状态"、"运行状态"、"完成状态"
     */
    String getStateName();
}

package com.design.mode.structural.bridge;

/**
 * 颜色接口（Implementor）- 实现部分
 * 
 * 桥接模式中的实现部分接口，定义了实现类的接口。
 * 这个接口不一定要与抽象部分的接口完全相同，实际上两者可以完全不同。
 * 
 * 桥接模式的核心思想：
 * - 将抽象部分与实现部分分离，使它们可以独立变化
 * - 使用组合而非继承来连接两个维度
 * - 解决多继承带来的类爆炸问题
 * 
 * 实现部分特点：
 * - 定义实现类的接口
 * - 可以有多个具体实现类
 * - 独立于抽象部分变化
 * 
 * 在本示例中：
 * - Color 是实现部分接口
 * - Red 和 Blue 是具体实现类
 * - 形状（Shape）是抽象部分
 * - 两者通过桥接连接，可以独立变化
 */
public interface Color {
    
    /**
     * 应用颜色
     * 
     * 这是实现部分的核心方法，具体实现类需要实现这个方法来提供颜色功能。
     * 抽象部分（Shape）会调用这个方法来获取颜色信息。
     * 
     * @return 颜色名称，如"红色"、"蓝色"
     */
    String applyColor();
}

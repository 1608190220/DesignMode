package com.design.mode.structural.bridge;

/**
 * 形状抽象类（Abstraction）- 抽象部分
 * 
 * 桥接模式中的抽象部分，定义了抽象类的接口，维护一个对实现部分的引用。
 * 抽象部分通过组合的方式持有实现部分的引用，这就是桥接。
 * 
 * 桥接模式的关键：
 * - 抽象部分与实现部分分离
 * - 使用组合而非继承
 * - 两个维度可以独立变化
 * 
 * 抽象部分特点：
 * - 定义抽象类的接口
 * - 持有实现部分的引用（Color）
 * - 可以有扩充抽象类（RefinedAbstraction）
 * - 独立于实现部分变化
 * 
 * 设计原则体现：
 * - 组合优于继承：通过组合而非继承来连接两个维度
 * - 开闭原则：可以独立地扩展抽象部分和实现部分
 * - 依赖倒置原则：依赖于抽象（Color接口）而非具体实现
 * 
 * 在本示例中：
 * - Shape 是抽象部分
 * - Circle 和 Square 是具体抽象部分
 * - Color 是实现部分
 * - 两者通过桥接连接，可以独立变化
 */
public abstract class Shape {
    
    /**
     * 持有颜色引用，这是桥接的关键
     * 
     * 使用 protected 修饰符，允许子类访问这个引用。
     * 这是桥接模式的核心：抽象部分持有实现部分的引用。
     * 
     * 使用组合而非继承的好处：
     * - 两个维度可以独立变化
     * - 添加新形状或新颜色都不会影响对方
     * - 避免了多继承带来的类爆炸问题
     */
    protected Color color;
    
    /**
     * 构造方法
     * 
     * 通过构造函数注入实现部分（Color），这是依赖注入的一种形式。
     * 
     * @param color 颜色实现部分，可以是 Red 或 Blue
     */
    public Shape(Color color) {
        this.color = color;
    }
    
    /**
     * 绘制方法
     * 
     * 这是抽象部分的核心方法，具体抽象部分需要实现这个方法。
     * 在实现中，会调用实现部分（Color）的方法来完成功能。
     * 
     * @return 绘制结果字符串
     */
    public abstract String draw();
}

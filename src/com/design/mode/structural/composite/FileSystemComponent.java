package com.design.mode.structural.composite;

/**
 * 文件系统组件接口（Component）
 * 
 * 组合模式中的抽象组件角色，定义组合中所有对象的通用接口。
 * 无论是叶子节点（File）还是复合节点（Directory），都实现这个接口。
 * 
 * 组合模式的核心思想：
 * - 将对象组合成树形结构以表示"部分-整体"的层次结构
 * - 使得用户对单个对象和组合对象的使用具有一致性
 * - 客户端无需区分是处理单个对象还是组合对象
 * 
 * 设计特点：
 * - 声明了组合对象的接口
 * - 在适当的情况下，实现所有类共有接口的默认行为
 * - 声明一个接口用于访问和管理子组件
 * 
 * 应用类比：
 * - 文件系统：文件和目录都可以看作文件系统组件
 * - 树形菜单：菜单项和子菜单
 * - 组织架构：员工和部门
 */
public interface FileSystemComponent {
    
    /**
     * 显示组件信息
     * 
     * 这是组合模式的核心方法之一，用于展示组件的信息。
     * 对于叶子节点（File），直接显示文件信息；
     * 对于复合节点（Directory），先显示目录信息，再递归显示所有子组件。
     * 
     * @param depth 深度，用于缩进显示，表示当前组件在树形结构中的层级
     */
    void display(int depth);
    
    /**
     * 获取组件大小
     * 
     * 对于叶子节点（File），返回文件本身的大小；
     * 对于复合节点（Directory），递归计算所有子组件的大小之和。
     * 
     * @return 组件大小，单位为字节
     */
    long getSize();
    
    /**
     * 处理组件
     * 
     * 这是一个通用的业务处理方法，展示组合模式的一致性处理能力。
     * 对于叶子节点（File），处理文件本身；
     * 对于复合节点（Directory），先处理目录，再递归处理所有子组件。
     */
    void process();
}
